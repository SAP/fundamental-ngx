/**
 * Represents the positioning of the modal on the screen.
 */
export class ModalPosition {

    /** Value of the modal's top position. */
    top?: string;

    /** Value of the modal's bottom position. */
    bottom?: string;

    /** Value of the modal's left position. */
    left?: string;

    /** Value of the modal's right position. */
    right?: string;
}
