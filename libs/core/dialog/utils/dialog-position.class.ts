/**
 * Represents the positioning of the dialog on the screen.
 */
export class DialogPosition {
    /** Value of the dialog's top position. */
    top?: string | number;

    /** Value of the dialog's bottom position. */
    bottom?: string | number;

    /** Value of the dialog's left position. */
    left?: string | number;

    /** Value of the dialog's right position. */
    right?: string | number;
}
